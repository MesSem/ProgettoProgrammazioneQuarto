package classes;

public class NotUsedPieceConfigurationException extends Exception {
	public NotUsedPieceConfigurationException() {
		super();
	}

	public NotUsedPieceConfigurationException(String message) {
		super(message);
	}

	public NotUsedPieceConfigurationException(String message, Throwable cause) {
		super(message, cause);
	}

	public NotUsedPieceConfigurationException(Throwable cause) {
		super(cause);
	}
}
